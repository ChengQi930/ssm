public class CrmInfo {

			private String userId;
		
			private String crmUserName;
			
			private String crmUserKey;

			public String getUserId() {
				return userId;
			}

			public void setUserId(String userId) {
				this.userId = userId;
			}

			public String getCrmUserName() {
				return crmUserName;
			}

			public void setCrmUserName(String crmUserName) {
				this.crmUserName = crmUserName;
			}

			public String getCrmUserKey() {
				return crmUserKey;
			}

			public void setCrmUserKey(String crmUserKey) {
				this.crmUserKey = crmUserKey;
			}

			
			
			
			
}
